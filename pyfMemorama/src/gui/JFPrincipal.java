    private void jbIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbIniciarActionPerformed
        voltearCartas();
        if ("Finalizar".equals(jbIniciar.getText())) {
            jbIniciar.setText("Nueva partida");
            jbIniciar.setMnemonic('N');
            jcbTematica.setEnabled(true);
            jtNomJugador.setEnabled(true);
            jlIntentos.setText("0");
            jugando = false;
        } else {
            jbIniciar.setText("Finalizar");
            jbIniciar.setMnemonic('F');
            jcbTematica.setEnabled(false);
            jtNomJugador.setEnabled(false);
            jlIntentos.setText("0");
            jugando = true;
            cartasDisp = cantCeldas;
            iniciaJuego(jcbTematica.getSelectedIndex());
        }
    }//GEN-LAST:event_jbIniciarActionPerformed

