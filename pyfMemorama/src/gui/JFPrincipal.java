    private void iniciaJuego(int tematica) {
        tab1 = new Tablero(tematica, cantCeldas, jtNomJugador.getText());
        tab1.setCartas();
        // Extrae los componentes de jpCeldas (botones), identifica el orden con
        // getName y le asigna la imagen que le corresponde a cada botón.
        JButton jbC = new JButton();
        for (int i = 1; i <= cantCeldas; i++) {
            for (int j = 0; j < cantCeldas; j++) {
                if (i == Integer.parseInt(jpCeldas.getComponent(j).getName())) {
                    jbC = (JButton) jpCeldas.getComponent(j);
                    jbC.setIcon(new ImageIcon(getClass().getResource(tab1.getCartas().get(i - 1).getImgContraCara())));
                    jbC.setDisabledIcon(new ImageIcon(getClass().getResource(tab1.getCartas().get(i - 1).getImgCarta())));
                }
            }
        }
        tab1.setTiempoIni(LocalTime.now());
        //Comienza el juego.
    }

    private void jbEnabled(JButton jb, int posCelda) {
        if (jbCartasVisibles[0] != null && jbCartasVisibles[1] != null) {
            jbCartasVisibles[0].setEnabled(true);
            jbCartasVisibles[1].setEnabled(true);
            jbCartasVisibles[0] = jb;
            posCeldaA = posCelda;
            jb.setEnabled(false);
            jbCartasVisibles[1] = null;
        } else {
            if (jbCartasVisibles[0] == null && jbCartasVisibles[1] == null) {
                jb.setEnabled(false);
                jbCartasVisibles[0] = jb;
                posCeldaA = posCelda;
            } else {
                jb.setEnabled(false);
                jbCartasVisibles[1] = jb;
                posCeldaB = posCelda;
                jlIntentos.setText(String.valueOf(Integer.parseInt(jlIntentos.getText()) + 1));
                // Compara cartas
                if (tab1.comparaCartas(posCeldaA, posCeldaB)) {
                    jbCartasVisibles[0] = null;
                    jbCartasVisibles[1] = null;
                    cartasDisp -= 2;
                    // Suma puntuacion
                    tab1.puntuacion(posCeldaA, posCeldaB);
                    if (cartasDisp == 0) {
                        finalizarJuego();
                    }
                }
            }
        }
    }

    private void finalizarJuego() {
        tab1.setTiempoTranscurrido();
        JOptionPane.showMessageDialog(this, "El juego ha finalizado."
                + tab1.toString(), "\nFELICIDADES!!!", JOptionPane.INFORMATION_MESSAGE);
        rankear();
        jbIniciar.setText("Nueva partida");
        jbIniciar.setMnemonic('N');
        jcbTematica.setEnabled(true);
        jtNomJugador.setEnabled(true);
        jtNomJugador.setText("");
        jugando = false;
    }

    private void rankear() {
        String[] fila = new String[4];
        fila[0] = tab1.getNomJugador();
        fila[1] = String.valueOf(tab1.getPuntaje());
        fila[2] = tab1.getTiempoFin();
        fila[3] = jlIntentos.getText();
        tabla.addRow(fila);
        jtRanking.setModel(tabla);
    }

    private void voltearCartas() {
        JButton jbC = new JButton();
        for (int i = 0; i < cantCeldas; i++) {
            jbC = (JButton) jpCeldas.getComponent(i);
            jbC.setEnabled(true);
        }
    }

